my_string = "Hello, World!"

# String Indexing, it's used to access individual characters. Using []
print(my_string[0])
print(my_string[7])

# String Slicing allows us to access a range of characters in a string. syntax [start:stop:step]
print(my_string[0:5])
print(my_string[7:])
print(my_string[:5])
print(my_string[::2])  # it steps every second character. we can call this step or stride

# We use len() method to find out the length of the string.
print(len(my_string))

